package bean;

public class InregralBean {
	private String total_integral;
	private double integral;
	private String type;
	private String desc;
	private long conversion_time;
	private String offer_name;
	private String logo;

	public String getTotal_integral() {
		return total_integral;
	}

	public void setTotal_integral(String total_integral) {
		this.total_integral = total_integral;
	}

	public double getIntegral() {
		return integral;
	}

	public void setIntegral(double integral) {
		this.integral = integral;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public long getConversion_time() {
		return conversion_time;
	}

	public void setConversion_time(long conversion_time) {
		this.conversion_time = conversion_time;
	}

	public String getOffer_name() {
		return offer_name;
	}

	public void setOffer_name(String offer_name) {
		this.offer_name = offer_name;
	}

	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

}
